			NOPL Direct Funding
			Millage #2: 2.33 (about $9.0 million)
			''Library and Early Childhood Fund'': 0.987 (about $3.8 million)" class="full-width">

			<h2>Do the proposed millage reallocations lower taxes?</h2>
			<p>
				No. The three propositions on the December 5 ballot collectively keep current millage rates exactly the same.
			</p>

			<h2>So what’s wrong with proposition #2?</h2>
			<p>We feel there are several ways in which prop #2 works against our community interests.</p>

			<ul>
				<li>
					Prop #2 cuts about 40% of the New Orleans Public Library budget for the next 20 years. This represents a massive, disproportionate cut that would result in reduced services, personnel lay-offs and branch closures. New Orleans loves libraries and have voted for this funding repeatedly. Now is not the time to take resources away from underserved communities.
				</li>
				<li>
					The reduced funding doesn’t go directly to the library like the current millage. Instead, the money goes through a “library and early childhood education” fund. Voters do not have adequate information about how this fund would function. It is clear, however, that prop #2 stands to compromise the NOPL’s independence.
				</li>
				<li>
					Most of the millions of taxpayer dollars cut from the library system wind up in an economic development slush fund. Voters don’t have nearly enough information about this fund, and the lack of transparency and accountability should be worrying to all New Orleans citizens.
				</li>
				<li>
					Prop #2 creates a false choice between early childhood education and libraries. We’re all for the proposed $1.5 million for early childhood education, but NOT when $7.5 million is being cut from libraries, which support early childhood education in their own right. 
				</li>
			</ul>

			<p>
				We’ll go into more detail about each of these points in this factsheet. But here’s the bottom line: prop #2, along with the rest of the measures on the December 5 ballot, is a raw deal for voters. There’s a reason that New Orleans has overwhelmingly supported the current millage twice before. We love our libraries, and know they make for better, stronger communities.
			</p>

			<p>
				<strong>On December 5, we’re asking voters to continue funding their libraries and to vote NO on proposition #2.</strong>
			</p>

