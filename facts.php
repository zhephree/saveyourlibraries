			<h2>What exactly does proposition #2 do?</h2>
			<p>
				The New Orleans Public Library is funded by property taxes — also known as millages. Prop #2 would affect one millage in particular, which accounts for a little over half of the NOPL budget with a millage rate of 2.58, equivalent to about $10 million in 2020.
			</p>
			<p>
				If proposition #2 were to pass, this particular millage would be eliminated. In its place, prop #2 would create a new “library and early childhood education” fund. This fund would receive a much smaller millage, with a rate of 0.987, or about $3.8 million in 2021.
			</p>
			<img src="/img/facts2.png" alt="CURRENT
			NOPL Direct Funding
			Millage #1: 2.58 (about $10.0 million)
