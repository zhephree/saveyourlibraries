			<h2>What is Proposition #2?</h2>
			<p>
				Proposition #2 is a measure on the December 5, 2020 ballot that would cut the New Orleans Public Library's budget by 40% for the next 20 years.
			</p>
			<p>
				Advocates for prop #2 claim that the measure would “renew” library funding and support early childhood education. But this simply isn’t true. The reality is that prop #2 cuts library funding, allocates a very small amount to early childhood funding, and creates a huge economic development slush fund. Libraries and early childhood education deserve better. 
			</p>
